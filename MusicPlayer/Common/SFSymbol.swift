enum SFSymbol: String {
    /// 재생 버튼 이미지
    case play = "play.fill"
    /// 정지 버튼 이미지
    case pause = "pause.fill"
    /// 이전 곡 버튼 이미지
    case backward = "backward.fill"
    /// 다음 곡 버튼 이미지
    case forward = "forward.fill"
    /// 랜덤 재생 버튼 이미지
    case shuffle = "shuffle"
    /// 반복 재생 버튼 이미지
    case repeatAll = "repeat"
}
